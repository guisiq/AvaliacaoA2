package model;

public class Tecido {
	private Integer id;
	private String tipoTecido;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getTipoTecido() {
		return tipoTecido;
	}
	public void setTipoTecido(String tipoTecido) {
		this.tipoTecido = tipoTecido;
	}
	
	
}
